package Test;

public class AnimalsNames {
    protected String name;
    protected int weight;
    protected int speed;

    public AnimalsNames(String name, int weight, int speed) {
        this.name = name;
        this.weight = weight;
        this.speed = speed;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
      if (name != null && name.length() > 2) {
          this.name = name;
      }
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }
}
